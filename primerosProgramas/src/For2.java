public class For2 {
    public static void main(String[] args) throws Exception {
        System.out.println("Tabla del 7: ");
        for (int i = 0; i < 11; i++) {
            int num = 7;
            num *= i;
            System.out.println("7 x " + i + " = " + num);
            num = 7;
        }
    }
}
